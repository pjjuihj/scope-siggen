/* ADC完成回调函数（DMA传输完成：后半段就绪） */
volatile uint32_t adc_cplt_count = 0;
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
    if (hadc->Instance == ADC1) {
        adc_cplt_count++;
        /* 设置可处理数据指针（后半段） */
        extern uint16_t adc_buffer[];
        extern volatile uint16_t *process_ptr;
        extern volatile uint16_t process_len;
        extern volatile uint32_t osc_config_buffer_size;

        uint32_t half = osc_config_buffer_size / 2;
        process_ptr = &adc_buffer[half];
        process_len = half;

        /* 通知示波器任务 */
        extern osThreadId_t oscilloscope_task_handle;
        if (oscilloscope_task_handle != NULL) {
            BaseType_t xHigherPriorityTaskWoken = pdFALSE;
            vTaskNotifyGiveFromISR(oscilloscope_task_handle, &xHigherPriorityTaskWoken);
            portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
        }
    }
}

/* ADC半传输回调函数（DMA半传输完成：前半段就绪） */
volatile uint32_t adc_half_count = 0;
void HAL_ADC_ConvHalfCpltCallback(ADC_HandleTypeDef* hadc)
{
    if (hadc->Instance == ADC1) {
        adc_half_count++;
        /* 设置可处理数据指针（前半段） */
        extern uint16_t adc_buffer[];
        extern volatile uint16_t *process_ptr;
        extern volatile uint16_t process_len;
        extern volatile uint32_t osc_config_buffer_size;

        process_ptr = &adc_buffer[0];
        process_len = osc_config_buffer_size / 2;

        /* 通知示波器任务 */
        extern osThreadId_t oscilloscope_task_handle;
        if (oscilloscope_task_handle != NULL) {
            BaseType_t xHigherPriorityTaskWoken = pdFALSE;
            vTaskNotifyGiveFromISR(oscilloscope_task_handle, &xHigherPriorityTaskWoken);
            portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
        }
    }
}

